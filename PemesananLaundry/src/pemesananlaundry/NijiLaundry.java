    private void cb_jenisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_jenisActionPerformed
        // TODO add your handling code here:
        if(cb_jenis.getSelectedItem().equals("Cuci Kering")){
            txt_harga.setText("4000");
            }else if (cb_jenis.getSelectedItem().equals("Cuci Setrika")){
                txt_harga.setText("6000");
            }else if (cb_jenis.getSelectedItem().equals("Setrika")){
                txt_harga.setText("4500");
            }
    }//GEN-LAST:event_cb_jenisActionPerformed

    private void txt_beratKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_beratKeyReleased
        // TODO add your handling code here:
        
        int Harga = Integer.parseInt(txt_harga.getText());
        int Berat = Integer.parseInt(txt_berat.getText()); 
        int Total = Harga * Berat;
        txt_total.setText(String.valueOf(Total));
        
    }//GEN-LAST:event_txt_beratKeyReleased

