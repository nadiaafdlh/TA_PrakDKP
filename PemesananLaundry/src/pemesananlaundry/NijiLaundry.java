    private void btn_checkoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_checkoutActionPerformed
        // TODO add your handling code here:
        int bayar = Integer.parseInt(txt_bayar.getText());
        int total = Integer.parseInt(txt_total.getText());
        int kembalian = bayar - total;
        String Kembalian = String.valueOf(kembalian);
        JOptionPane.showMessageDialog(null, "Kembalian Anda : " + Kembalian);
        
        JOptionPane.showMessageDialog(null, "Terima kasih sudah memesan jasa Niji Laundry, " );
      
           
    }//GEN-LAST:event_btn_checkoutActionPerformed

